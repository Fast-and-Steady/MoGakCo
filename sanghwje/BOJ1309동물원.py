# 아무것도 없는 경우, 왼쪽에 배치되는 경우, 오른쪽에 배치되는 경우 
N = int(input())
d = [[ 0 for _ in range(3)] for _ in range(N+1)]
d[1][0] = d[1][1] = d[1][2] = 1
for i in range(2,N+1):
    d[i][0] = d[i-1][0]%9901 +  d[i-1][1]%9901 + d[i-1][2]%9901
    d[i][1] =  d[i-1][0]%9901+ d[i-1][2]%9901
    d[i][2] =  d[i-1][1]%9901 + d[i-1][0]%9901 
print(sum(d[N])%9901)  

